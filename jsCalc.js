function add(num1, num2){
    return num1+num2;
}
function subtract(num1, num2){
    return num1-num2;
}
function multiply(num1, num2){
    return num1*num2;
}
function divide(num1, num2){
    return num1/num2;
}   


var sum = add(4,2);
var diff = subtract(4,2);
var product = multiply(4,2);
var dividend = divide(4,2);

console.log("These are the Results: " + sum + "  " + diff + "  " + product + "  " + dividend)